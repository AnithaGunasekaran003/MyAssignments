package week1.day2;

public class LearnSwitch {

	public static void main(String[] args) {
		
		int day = 2;
		
		switch(day)
		{
		
		case 1:
			System.out.println("Today is Monday");
			break;
		case 2:
			System.out.println("Today is Tuesday");
			break;
			default:
				System.out.println("Today is Sunday");
		}
	}
}
